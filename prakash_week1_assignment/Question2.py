# Problem Statement:
# You are given two integers, a and b, as input. Your task is to perform and print the following operations:
# 1. Print the sum of a and b.
# 2. Print the difference when b is subtracted from a (i.e., a - b).
# 3. Print the product of a and b.

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print(f"The sum of a and b is: {a + b}")
print(f"The difference of a and b is: {a - b}")
print(f"The product of a and b is: {a * b}")